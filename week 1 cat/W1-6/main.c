#include <stdio.h>

int main() {
    int x=7,y =4;

    printf("(x + y) = %d\n",(x+y));
    printf("(x - y) = %d\n",(x-y));
    printf("(x * y) = %d\n",(x*y));
    printf("(x / y) = %d\n",(x/y));
    printf("(x > y) ?= %d\n",(x>y));
    printf("(x < y) = %d\n",(x<y));
    printf("(x = y) = %d\n",(x==y));
    printf("(x | y) = %d\n",(x|y));
    printf("(x & y) = %d\n",(x&y));
    printf("(x ^ y) = %d\n",(x^y));
    printf("(x << 1) = %d\n",(x<<1));
    printf("(x >> 2) = %d\n",(x>>2));

}
